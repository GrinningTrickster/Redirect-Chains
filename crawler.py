def random_desktop_agent():
    agent = choice(DesktopAgents)
    return {'User-Agent': agent}


def html_request(url):
    try:
        r = requests.get(url, headers=random_desktop_agent(), timeout=15, allow_redirects=False)
    except requests.ConnectTimeout:
        r = requests.get(url, headers=random_desktop_agent(), timeout=60, allow_redirects=False)
    except:
        url = url
        html = None
        status_code = None
        return url, html, status_code
    return r.url, r.text, r.status_code

